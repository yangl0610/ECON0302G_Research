def get_controlled_ids(civ_name: str, territories: float) -> List[int]:
    """
    给定文明名和领土标量，返回该文明控制的 ISO 国家代码列表。

    territories = 1.0 → 本土
    territories = 2.0 → 本土 + 约半数殖民目标
    territories = 3.0+ → 本土 + 全部殖民目标（上限：max 殖民地数量）
    """
    home     = HOME_TERRITORIES.get(civ_name, [])
    colonial = COLONIAL_TARGETS.get(civ_name, [])

    if territories <= 1.0 or not colonial:
        return list(home)

    # 殖民地数量与 (territories - 1) 成正比
    # territories = 2.0 → 获得 colonial 的 1/2
    # territories = 3.0 → 获得 colonial 的全部
    max_extra = len(colonial)
    n_extra   = min(int(round((territories - 1.0) * max_extra * 0.6)), max_extra)

    return list(home) + colonial[:n_extra]


def build_control_df(year_df: pd.DataFrame) -> pd.DataFrame:
    """
    输入：某一年所有文明的快照 DataFrame（必须含 civilization、territories 列）
    输出：id (int) | civilization (str) 的 DataFrame

    冲突处理：若同一国家被多个文明声索，保留 GDP 最高的文明（军事实力更强）。
    这模拟了历史上的"实际控制权"归 GDP/军事更强的一方。
    """
    # 先按 GDP 降序排列，使强国优先占领
    year_df_sorted = year_df.sort_values("gdp", ascending=False)

    claimed: Dict[int, str] = {}  # country_id → civilization
    for _, row in year_df_sorted.iterrows():
        civ  = row["civilization"]
        terr = float(row["territories"])
        for country_id in get_controlled_ids(civ, terr):
            if country_id not in claimed:
                claimed[country_id] = civ

    if not claimed:
        return pd.DataFrame(columns=["id", "civilization"])

    return pd.DataFrame([
        {"id": cid, "civilization": civ}
        for cid, civ in claimed.items()
    ])
